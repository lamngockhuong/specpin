#!/usr/bin/env node
// Generate the live extension's CSS token layer from design-tokens.json.
//
// design-tokens.json is the single source of truth for both the .pen mockups
// (see sync-tokens.mjs) AND the shipped extension UI. This script emits
// ../src/shared/tokens.gen.css: a `:root` block (shared tokens + light theme) plus a
// `@media (prefers-color-scheme: dark)` override (dark theme), so the UI flips
// theme with zero JS.
//
// The same file serves two consumers:
//   - popup + options HTML pages: imported normally (Vite injects it).
//   - Shadow DOM renderers (sidebar/tooltip/capture-form): imported with Vite's
//     `?inline` query and prepended to each renderer's STYLES, because
//     `:host { all: initial }` blocks inherited custom properties from the host
//     page. `:root` inside an injected <style> matches the shadow root, and
//     @media works inside shadow DOM, so this file applies verbatim there too.
//
// Run after editing design-tokens.json:  node sync-css-tokens.mjs
// Do NOT hand-edit src/shared/tokens.gen.css; it is overwritten by this script.

import { readFileSync, writeFileSync } from "node:fs";

const dir = new URL("./", import.meta.url);
const tokens = JSON.parse(readFileSync(new URL("design-tokens.json", dir)));
const OUT = new URL("../src/shared/tokens.gen.css", dir);

// camelCase / digit-suffixed keys -> kebab var fragment: text2 -> text-2,
// successDot -> success-dot, gradTop -> grad-top.
const kebab = (key) =>
  key
    .replace(/([a-z])([A-Z])/g, "$1-$2")
    .replace(/([a-zA-Z])(\d)/g, "$1-$2")
    .toLowerCase();

// Shared (theme-agnostic) declarations, emitted once in :root. Font stacks list
// the canonical face first, then resilient fallbacks; web fonts are not bundled
// yet (tracked as a 1.1 follow-up), so these degrade gracefully.
const sharedDecls = [
  `  --sp-accent: ${tokens.brand.base};`,
  `  --sp-accent-hover: ${tokens.brand.hover};`,
  `  --sp-accent-on: ${tokens.brand.on};`,
  `  --sp-brand-glow: ${tokens.brand.glow};`,
  `  --sp-font-ui: ${tokens.font.ui}, system-ui, -apple-system, sans-serif;`,
  `  --sp-font-mono: "${tokens.font.mono}", ui-monospace, "SFMono-Regular", monospace;`,
  `  --sp-radius-card: ${tokens.radius.card}px;`,
  `  --sp-radius-control: ${tokens.radius.control}px;`,
];
if (tokens.overlay) sharedDecls.push(`  --sp-overlay-bg: ${tokens.overlay};`);

// Per-theme color declarations (one block per theme axis value).
const themeDecls = (mode) =>
  Object.entries(tokens.themes[mode]).map(([key, val]) => `  --sp-${kebab(key)}: ${val};`);
const light = themeDecls("light");
const dark = themeDecls("dark");

const header = [
  "/* GENERATED by apps/extension/designs/sync-css-tokens.mjs - DO NOT EDIT. */",
  "/* Source of truth: apps/extension/designs/design-tokens.json */",
  "/* Regenerate: pnpm --filter @specpin/extension sync-css-tokens */",
].join("\n");

const css = `${header}

:root {
${sharedDecls.join("\n")}

${light.join("\n")}
}

@media (prefers-color-scheme: dark) {
  :root {
${dark.map((line) => `  ${line}`).join("\n")}
  }
}
`;

writeFileSync(OUT, css);
console.log(
  `Wrote src/shared/tokens.gen.css (${sharedDecls.length + light.length} vars in :root, ${dark.length} dark overrides).`,
);
